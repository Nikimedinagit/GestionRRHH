
////////////////////////////////////////////////////////////////////////////////////////////////////////
// OBTENER LOS DATOS DE LA API DE SECTORES ////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////
async function ObtenerSectoresDropDown() {
    const res = await authFetch("Sector/Activos", {
        method: "GET",
    })
        .then(response => response.json())
        .then(data => {
            MostrarSectoresDropDown(data)
        })
        .catch((error) => {
            MostrarErrorCatch();
        });
}


////////////////////////////////////////////////////////////////////////////////////////////////////////
// FUNCIÓN PARA MOSTRAR LOS DATOS DE LA API DE SECTORES ///////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////
function MostrarSectoresDropDown(data) {
    const $dropdown = $('#IdSector');
    $dropdown.empty();

    $dropdown.append(`<option value="" selected disabled hidden>Seleccione</option>`);

    $.each(data, function (index, item) {
        $dropdown.append(`<option value="${item.id}">${item.nombre}</option>`);
    });
}


////////////////////////////////////////////////////////////////////////////////////////////////////////
/// INICILAR AL CARGAR LA VISTA ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////
ObtenerSectoresDropDown();